package com.migu.model;

public class PropertyFile {
    private String propertyKey;
    private String propertyValue;
    private String propertyItem;
    private String propertyShow;

    public String getPropertyKey() {
        return propertyKey;
    }

    public void setPropertyKey(String propertyKey) {
        this.propertyKey = propertyKey;
    }

    public String getPropertyValue() {
        return propertyValue;
    }

    public void setPropertyValue(String propertyValue) {
        this.propertyValue = propertyValue;
    }

    public String getPropertyItem() {
        return propertyItem;
    }

    public void setPropertyItem(String propertyItem) {
        this.propertyItem = propertyItem;
    }

    public String getPropertyShow() {
        return propertyShow;
    }

    public void setPropertyShow(String propertyShow) {
        this.propertyShow = propertyShow;
    }
}
